size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	int		j;

	i = 0;
	j = 1;
	while (i < dstsize - 1 && j)
	{
		if (*src != '\0')
		{
			*dst++ = *src++;
		}
		else
		{
			j = 0;
		}
		i++;
	}
	*dst = '\0';
	return (i);
}
